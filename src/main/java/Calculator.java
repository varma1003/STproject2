public class Calculator {
    public Calculator(){

    }
    public int addition(int a, int b){
        int add=a+b;
        return add;
    }
    public int subtraction(int a, int b){
        int sub=a-b;
        return sub;
    }
    public int multiplication(int a, int b){
        int mul=a*b;
        return mul;
    }
}
